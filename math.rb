Matemática
Ruby não se limita a expressões simples de atribuição como my_num = 100; ele também pode fazer
 toda a matemática que você aprendeu na escola.

Existem seis operadores aritméticos nos quais vamos nos concentrar:

Adição ( +)

Subtração ( -)

Multiplicação ( *)

Divisão ( /)

Exponenciação ( **)

Módulo ( %)

Os únicos que provavelmente parecem estranhos para você são exponenciação e módulo. 
A exponenciação eleva um número (a base) à potência do outro (o expoente).
 Por exemplo, 2**3é 8, pois 2**3significa “me dê 2 * 2 * 2” (2 multiplicado por 3 vezes). 3**2é 9(3 * 3), 
 e assim por diante.

Módulo retorna o restante da divisão. Por exemplo, 25 % 7seria 4, já que 7 cabe em 25 três vezes com 4 sobrando.