# Variáveis
# Um dos conceitos mais básicos na programação de computadores é a variável . 
# Você pode pensar em uma variável como uma palavra ou nome que abrange um único valor. 
# Por exemplo, digamos que você precise do número 25 do nosso último exemplo, mas não vai usá-lo imediatamente. 
# Você pode definir uma variável, digamos my_num, para obter o valor 25 e mantê-lo para uso posterior, assim:

my_num = 25


# Declarar variáveis ​​em Ruby é fácil: basta escrever um nome como my_num, usar =para atribuir um valor e pronto!
#  Se você precisar alterar uma variável, não se preocupe: basta digitá-la novamente e clicar =para atribuir 
#  um novo valor a ela.

