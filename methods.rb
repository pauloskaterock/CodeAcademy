# O método '.length'
# Os métodos são convocados usando um arquivo .. Se você tiver uma string, "I love espresso", 
# e pegar o .lengthdela, Ruby retornará o comprimento da string 
# (isto é, o número de caracteres — letras, números, espaços e símbolos). Confira:

"I love espresso".length
# ==> 15 
# (Aquela pequena linha começando com #não faz parte do que você precisa escrever - ela apenas 
# mostra a saída que o Ruby fornecerá. Mais sobre isso na próxima seção!)

# Tomar o tamanho da entrada pode ser útil se, por exemplo, 
# você quiser fazer um site que aceita pagamentos com cartão de crédito. 
# Ruby pode verificar se o número do cartão de crédito parece ser válido.


# O método '.reverse'
# O .reversemétodo é chamado da mesma forma .length, mas em vez de pedir ao 
# Ruby para dizer quanto tempo é uma string, 
# ele cospe uma versão reversa da string que você forneceu. Por exemplo,

"Eric".reverse
# vai resultar em

"cirE"
# Inverter a entrada pode ser útil se você quiser classificar uma lista de valores 
# do maior para o menor em vez do menor para o maior. (Vamos começar a classificar em lições posteriores.)

